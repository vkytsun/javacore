public class ArithmeticOperations {
    public static int multiply(int a, int b) {
        return a * b;
    }

    public static int division(int a, int b) {
        return a / b;
    }

    public static int addition(int c, int d) {
        return c - d;
    }

    public static int subtraction(int e, int f) {
        return e - f;
    }
}
